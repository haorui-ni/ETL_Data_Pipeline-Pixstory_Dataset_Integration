
create or replace TABLE PIXSTORY_DATA.PIXSTORY.PIXSTORY (
	PK_ID NUMBER(38,0) NOT NULL,
	STORY_PRIMARY_ID NUMBER(38,0),
	STORY_ID VARCHAR(255),
	USER_PRIME_ID NUMBER(38,0),
	USER_ID VARCHAR(255),
	GENDER VARCHAR(255),
	AGE NUMBER(38,0),
	TITLE VARCHAR(16777216),
	NARRATIVE VARCHAR(16777216),
	MEDIA VARCHAR(16777216),
	ACCOUNT_CREATED_DATE DATE,
	INTEREST VARCHAR(16777216),
	primary key (PK_ID)
);

create or replace TABLE PIXSTORY_DATA.PIXSTORY.IMAGE (
	PK_ID NUMBER(38,0) NOT NULL,
	IMAGE_CAP VARCHAR(1000),
	IMAGE_OBJ VARCHAR(1000),
	primary key (PK_ID),
	foreign key (PK_ID) references PIXSTORY_DATA.PIXSTORY.PIXSTORY(PK_ID)
);

create or replace TABLE PIXSTORY_DATA.PIXSTORY.HATE_SPEECH (
	GLAAD_SYMBOL VARCHAR(16777216),
	ADL_SYMBOL VARCHAR(16777216)
);

create or replace TABLE PIXSTORY_DATA.PIXSTORY.FLAG (
	PK_ID NUMBER(38,0) NOT NULL,
	GLAAD_FLAG VARCHAR(255),
	ADL_FLAG VARCHAR(255),
	primary key (PK_ID),
	foreign key (PK_ID) references PIXSTORY_DATA.PIXSTORY.PIXSTORY(PK_ID)
);